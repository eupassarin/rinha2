CREATE UNLOGGED TABLE IF NOT EXISTS C (
     I SMALLINT,
     L INT NULL,
     S INT DEFAULT 0 CONSTRAINT V check (S >= (L*-1))
);
CREATE INDEX PK_X ON C (I) INCLUDE (S);

