-- TRANSACAO
CREATE UNLOGGED TABLE IF NOT EXISTS T (
    I SMALLINT NULL,
    V INT NULL,
    P CHAR(1) NULL,
    D VARCHAR(10) NULL,
    R BIGINT default (extract(epoch from now()) * 1000)
);

CREATE INDEX C_X ON T (I);
CREATE INDEX R_X ON T (R DESC);
CLUSTER T USING C_X;



